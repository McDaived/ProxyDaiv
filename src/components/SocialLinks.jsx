function handleClick(e, deepLink, webLink) {
  const isMobile = /Android|iPhone|iPad|iPod/i.test(navigator.userAgent)
  if (!isMobile) return // let <a href> handle it on desktop

  e.preventDefault()
  window.location.href = deepLink

  setTimeout(() => {
    // If the app opened, the page becomes hidden — don't redirect
    if (!document.hidden) {
      window.location.href = webLink
    }
  }, 1500)
}

export default function SocialLinks() {
  const { t } = useLanguage()

  return (
    <section className="social-links">
      {SOCIALS.map(({ id, deepLink, webLink, icon }) => {
        const label = t.socialLabels[id]
        return (
          <a
            key={id}
            href={webLink}
            target="_blank"
            rel="noopener noreferrer"
            className={`social-btn social-btn--${id}`}
            onClick={(e) => handleClick(e, deepLink, webLink)}
            aria-label={label}
            title={label}
          >
            <span className="social-btn__icon">{icon}</span>
            <span className="social-btn__label">{label}</span>
          </a>
        )
      })}
    </section>
  )
}
